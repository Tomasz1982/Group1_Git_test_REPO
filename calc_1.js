function add(a, b) {
    return a + b;
}

function minus(a, b) {
    return a - b;
}

const result1 = add(4, 8);
console.log(`4 + 8 =  ${result1}`);

const result2 = add(6, 95);
console.log(`6 + 95 =  ${result2}`);

const result3 = minus(10, 6);
console.log(`10 - 6 =  ${result3}`);

const result4 = minus(17, 40);
console.log(`17 - 40 =  ${result4}`);
